    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        this.disableAllButton();
        String result = jTextArea3.getText();
        result = result.concat("\n---------------------- \n");
        if (!this.checkInputPolicy(policyApp)) {
            result = result.concat("Policy could not be found in source code.");
        } else if (!this.checkInputPolicy(policyDescription)) {
            result = result.concat("Policy could not be found in description.");
        } else {
            PolicyService policyService = new PolicyService();
            boolean checkAvailability = policyService.checkAvailability(policyDescription, policyApp).isIsSuccess();
            boolean checkConfidentiality = policyService.checkConfidentialityIntegrity(policyDescription, policyApp, "confidentiality").isIsSuccess();
            boolean checkIntegrity = policyService.checkConfidentialityIntegrity(policyDescription, policyApp, "integrity").isIsSuccess();
            if (checkAvailability && checkConfidentiality && checkIntegrity) {
                result = result.concat("WA conform with its specification.");
            } else {
                result = result.concat("WA doesn't conform with its specification.");
            }
        }
        jTextArea3.setText(result);
        this.activeAllButton();// TODO add your handling code here:
    }//GEN-LAST:event_jButton7ActionPerformed

